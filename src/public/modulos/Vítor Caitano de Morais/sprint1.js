info = (valor) => {
    var secaoDieta = document.getElementById("DTcabeca");
    secaoDieta.innerHTML = '';
    var marcarDieta = document.getElementById("DTchecks");
    marcarDieta.innerHTML = '';
    var secaoExercicios = document.getElementById("EXcabeca");
    secaoExercicios.innerHTML = '';
    var marcarExercicios = document.getElementById("EXchecks");
    marcarExercicios.innerHTML = '';
    var prog = document.getElementById("DTprogressao");
    prog.style.width = 0 + "%";
    var prog2 = document.getElementById("EXprogressao");
    prog2.style.width = 0 + "%";
    var taxas = []
    var taxasQueima = []
    var totalExercicios = 0

    let indexTaxa = 0;
    for (let a = 0; a < cadastro.refeições.length; a++)
    {
        for (let b = 0; b < valor.refeicoes.length; b++)
            if(cadastro.refeições[a].id == valor.refeicoes[b])
            {
                secaoDieta.innerHTML += `
                    ${cadastro.refeições[a].nome} - ${cadastro.refeições[a].calorias}kcal |`;
                marcarDieta.innerHTML += `
                    <input type="checkbox" id="00${b}">
                    <label for="00${b}">${cadastro.refeições[a].nome}</label>`;

                taxas[indexTaxa] = (cadastro.refeições[a].calorias / valor.calorias) * 100;
                indexTaxa++;
            }
        if (a < 3)
        {
            secaoExercicios.innerHTML += `
                ${cadastro.exercícios[a].tipo}: ${cadastro.exercícios[a].tempo} min - ${cadastro.exercícios[a].calorias}kcal |`;
            marcarExercicios.innerHTML +=`
                <input type="checkbox" id="1${a}">
                <label for="1${a}">${cadastro.exercícios[a].tipo}</label>`;

                taxasQueima[a] = cadastro.exercícios[a].calorias;
                totalExercicios += cadastro.exercícios[a].calorias;
        }
    }

    for (let c = 0; c < valor.refeicoes.length; c++)
    {

        let check = document.getElementById(`00${c}`);
        check.addEventListener("change", () => {
            if (check.checked)
                prog.style.width = ((parseFloat(prog.style.width) || 0) + (taxas[c])) + "%";
            else if (((parseFloat(prog.style.width) || 0) - (taxas[c])) > 0)
                prog.style.width = ((parseFloat(prog.style.width) || 0) - (taxas[c])) + "%";
            else
                prog.style.width = 0 + "%";
        })

        var prog2 = document.getElementById("EXprogressao");

        if (c < 3)
        {
            let check2 = document.getElementById(`1${c}`);
            check2.addEventListener("change", () => {
                if (check2.checked)
                    prog2.style.width = ((parseFloat(prog2.style.width) || 0) + (100 * taxasQueima[c] / totalExercicios)) + "%";
                else if ((parseFloat(prog2.style.width) || 0) - (100 * taxasQueima[c] / totalExercicios) > 0)
                    prog2.style.width = ((parseFloat(prog2.style.width) || 0) - (100 * taxasQueima[c] / totalExercicios)) + "%";
                else
                    prog2.style.width = 0 + "%";
            })
        }
    }
};


personalizaLoad = () => {
    const idEscolhido = Number(document.getElementById('controle').value)
    return cadastro.dietas.find(item => item.id == idEscolhido)
}

document.getElementById('controle').addEventListener('input', () => {
    var resultadoDieta = personalizaLoad(); 
    info(resultadoDieta); 
});


